#include "GameLevel.h"

void GameLevel::gamePlay()
{
//////////////////////////////////////////////////////////////////////////////////////////////////////
//                                      SPECIAL STEP
//////////////////////////////////////////////////////////////////////////////////////////////////////
    levelSpecialAction();

//////////////////////////////////////////////////////////////////////////////////////////////////////
//                                      LEVEL CHRONOMETER
//////////////////////////////////////////////////////////////////////////////////////////////////////
    if (m_countLevelTime)
    {
        if (m_gameTime.getTimeValue() != 0)
        {
            // level global time
            m_gameSysExt.m_levelTime += is::getMSecond(DELTA_TIME);
            m_gameTime.step(DELTA_TIME, is::VALUE_CONVERSION, is::VALUE_TIME);
        }
        else m_timeUp = true;
    }

//////////////////////////////////////////////////////////////////////////////////////////////////////
//                                              SDM
//////////////////////////////////////////////////////////////////////////////////////////////////////
    SDMstep(); // Let SDM manage the update of objects
}
